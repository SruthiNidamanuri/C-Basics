#include<stdio.h>
void main()
{
  //variable Declaration
   int n,s;
 //Initilization
   n=20,s=10;

   printf("%d>%d-->%d\n", n,s,n>s);
   printf("%d<%d-->%d\n", n,s,n<s);
   printf("%d>=%d-->%d\n", n,s,n>=s);
   printf("%d>=%d-->%d\n", n,s,n<=s);
   printf("%d==%d-->%d\n", n,s,n==s);
   printf("%d!=%d-->%d\n", n,s,n!=s);
}
