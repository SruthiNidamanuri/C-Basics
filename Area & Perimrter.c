#include<stdio.h>
void main()
{
  //variable Declaration
  int side, area, peri;
  
  side = 2;
  
  area = side * side;
  peri = 4 * side;
  
  printf(" Area of the square with side %d is: %d\n", side, area);
  printf(" Perimeter of the square with side %d is: %d", side, peri);
}
  
